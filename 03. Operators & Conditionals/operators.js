// UNARY Openarators

let a = 4;
console.log(a++);       // 4
console.log(a);         // 5


// Comparison Operators

a = 5;
let b = 10;

console.log("a == b ", a==b);    // false
console.log("a != b", a!=b);    // true
console.log("a > b", a > b);     // false
console.log("a < b",a < b);     // true


// Conditional Operators

let x = true;
let y = false; 

console.log("x && y", x && y);  // false
console.log("x || y", x || y);  // true
console.log("!x", !x);          // false
console.log("!y", !y);          // true
